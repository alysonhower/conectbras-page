    <h3 class="h3">
        <strong>6 - Otimizando o tempo</strong>
    </h3>
    <br />
    <p>
        Apesar de oferecer muitas vantagens, a verificação do documento é um
        processo relativamente demorado e custoso. A Conectbras verifica todos
        os documentos multiplas vezes antes de os repassar para seus clientes,
        porém, como o processo é predominantemente manual existe sempre a chance
        de passar um ou outro documento inapropriado e isso pode representar um
        problema futuro. Por tanto, para otimizarmos o tempo gasto processo de
        verificação dos documentos, podemos utilizar técnicas de avaliação por
        amostragem. Veja alguns pontos logo abaixo.
    </p>
    <br />
    <ol class="list-decimal list-inside space-y-4">
        <li>
            <strong>Sempre utilize o VeraPDF: </strong> atualmente o VeraPDF é a
            melhor ferramenta pública especificamente concebida para verificar a
            conformidade do padrão PDF-A e ela consegue verificar todos os documentos
            em todos os subníveis de pastas rapidamente. Por tanto, usar o VeraPDF
            otimizará todo o processo de verificação, aumentando a eficiência e garantindo
            a qualidade dos documentos.
        </li>
        <li>
            <strong
                >Priorize a verificação de certos documentos, ou verifique
                amostragens:
            </strong>
            a verificação das assinaturas com certificado digital e carimbo do tempo,
            provavelmente, é a parte mais demorada de todo o processo porque a única
            ferramenta pública disponibilizada por um ente confiável e que disponibiliza
            informações suficientes para este caso específico é o Verificar de Conformidade
            do ITI que atualmente se limita a 4 documentos por vez e muitos lotes
            de documentos passam facilmente de 4000 vias digitais dependendo da complexidade
            da execução do serviço e da disponibilidade da Conectbras; verificar
            todos levaria horas e potencialmente dias.
            <br />
            <br />
            Entretanto, o processo de aplicação de assinaturas e carimbos utilizados
            pela Conectbras é semi-automatizado, portanto, a chance de ocorrerem
            erros nesta etapa é muito baixa. Além disso, nos casos raríssimos onde
            algum erro no processo ocorre e a Conectbras não o identificou a tempo,
            a chance mais alta é de que todos os documentos organizados na mesma
            porção de arquivos estarão comprometidos. Sendo assim, a verificação
            por grupo de amostragem já seria suficiente para agregar mais segurança
            e confiabilidade.
            <br />
            <br />
            O número de documentos que devem ser analisádos é determinado inteiramente
            pelo interessado ou pelo setor responsável, porém, a Conectbras considera
            como razoável a verificação de ao menos um arquivo por grupo de documentos
            na mesma pasta, ou seja, um relatório por pasta ou malote de arquivos
            digitalizados.
        </li>
    </ol>
